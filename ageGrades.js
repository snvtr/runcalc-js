function prettyAgeGrade() {
  age_out.innerHTML = '';
  let distance = document.getElementById('age_select').value;
  let timeStr  = document.getElementById('age_time_input').value;
  let gender   = document.getElementById('age_gender_select').value;
  let age      = document.getElementById('age_age_input').value;
  age_out.innerHTML += String((100*gradedPercent(distance, gender, age, timeStr)).toFixed(2))+'%';
}

function getDistTime(distance, gender, age) {
  //returns top graded time in decimal minutes for distance + gender + age
  return strTimeToNum(AgeGrade[distance][gender])/AgeGrade[distance][String(age)+gender];
}

function gradedResult(distance, gender, age) {
  //gets distance+gender+age and returns formatted time string 
  return prettyTime(getDistTime(distance, gender, age));
}

function gradedPercent(distance, gender, age, timeStr) {
  //returns percentage of the best result for gender+age
  return getDistTime(distance, gender, age)/strTimeToNum(timeStr);
}

function prettyReverseAgeGrade() {
  //returns a list of probable results for a set of distances from age graded percentage
  
  let tbl = document.createElement('table');
  let tblBody = document.createElement('tbody');
  let gender   = document.getElementById('revage_gender_select').value;
  let age      = document.getElementById('revage_age_input').value;
  let pct      = document.getElementById('revage_coeff_input').value;

  for (let i of Object.keys(GRADED_D).sort((a,b)=>Number(a)-Number(b))) {
    let topAgeResult = strTimeToNum(AgeGrade[String(i)][gender])/AgeGrade[String(i)][String(age)+gender];
	  let gradedAgeResult = prettyTime(100*topAgeResult/pct);
    let row = document.createElement('tr');
    let cell1 = document.createElement('td');
    let cellText1 = document.createTextNode(GRADED_D[i][0]);
    let cell2 = document.createElement('td');
    let cellText2 = document.createTextNode(gradedAgeResult);
    cell1.appendChild(cellText1);
    cell2.appendChild(cellText2);
    row.appendChild(cell1);
    row.appendChild(cell2);
    tblBody.appendChild(row);
  }
  tbl.appendChild(tblBody);
  let revage_out = document.querySelector('div.revage_out');
  while (revage_out.firstChild) revage_out.removeChild(revage_out.firstChild);
  tbl.setAttribute('border', '0');
  tbl.setAttribute('cellpadding', '3');
  revage_out.appendChild(tbl);
}
